"""视频转像素精灵图 —— 统一配置"""

import os

# 项目根目录
ROOT = os.path.dirname(os.path.abspath(__file__))

# ============================================================
# 输入视频
# ============================================================
VIDEO_PATH = os.path.join(ROOT, "input", "action.mp4")
PROJECT = os.path.splitext(os.path.basename(VIDEO_PATH))[0]  # 自动取视频文件名

# ============================================================
# 第1步：抽帧
# ============================================================
FRAME_DIR = os.path.join(ROOT, "frames", PROJECT)

# ============================================================
# 第2步：像素化
#   pixel_size       — 缩小到的像素数（值越小像素块越大）
#   output_size      — 输出图片的边长（像素）
#   posterize_levels — 色调分离等级（0=关闭，建议 6~12）
# ============================================================
PIXEL_SIZE = 256
OUTPUT_SIZE = 512
POSTERIZE_LEVELS = 16

# ============================================================
# 第3步：去背景 + 去白边（Color Key 流程，对标 PS 魔棒+收缩选区）
#   method      — "colorkey": 自动检测背景色 → 颜色键透明（推荐）
#                — "white": 白色转透明（简易）
#                — "rembg": AI 去背景（需 pip install rembg）
#   tolerance   — 颜色容差（类似 PS 魔棒容差，值 0-255）
#   contract_px — 收缩选区：削掉主体最外层像素块（桌宠必备）
#   defringe_px — 去边宽度，像素小人建议 1，不要超过 2
# ============================================================
BG_METHOD = "colorkey"
TOLERANCE = 25
CONTRACT_PX = 1
DEFRINGE_PX = 1

# ============================================================
# 第4步：拼精灵图
#   cols — 每行列数（自动生成全帧 + 每4帧抽1帧 两份）
# ============================================================
SPRITE_COLS = 10

# ============================================================
# 并行处理
#   workers — 并行线程数（设为 0 自动取 CPU 核心数）
# ============================================================
WORKERS = 0

# ============================================================
# 输出目录（嵌套在项目名下，支持多工程并存）
# ============================================================
PIXELATED_DIR = os.path.join(ROOT, "output", "pixelated", PROJECT)
TRANSPARENT_DIR = os.path.join(ROOT, "output", "transparent", PROJECT)
SPRITE_DIR = os.path.join(ROOT, "output", "sprite", PROJECT)
